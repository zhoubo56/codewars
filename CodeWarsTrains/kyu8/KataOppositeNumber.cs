
namespace kyu8
{
    /// <summary>
    /// Opposite number
    /// https://www.codewars.com/kata/56dec885c54a926dcd001095
    /// </summary>
    public class KataOppositeNumber
    {
        public static int Opposite(int number)
        {
            //更好的方案 -number
            return number * -1;
        }
    }
}
